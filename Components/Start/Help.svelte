<p>
  Welcome to Qlorb. It's a game based on precision, reflexes and pure skills.
  The objective of the game is to get as many points as you can by interacting
  with the "orbs" that float on your screen.
</p>
<ul>
  <li>Click a <b>Green</b> or <b>Gold</b> orb with your left mouse button</li>
  <li>Click a <b>Red</b> orb with your right mouse button</li>
</ul>
<p>
  There are 10 levels, starting at 0. The levels are decided based on how many
  points you have. Each level has a different color. If you mis-click at any
  point, you'll lose upwards of a hundred points. If your score is already below
  100, it will be reset back down to 0.
</p>
<p>
  It may not seem difficult at first, but you are surely mistaken if you think
  that this is easy. So what are you waiting for? Hop on and experience this
  beast.
</p>
